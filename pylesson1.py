# Python uses indentation for blocks, instead of curly braces. Standard indentation requires standard python code to use four spaces.
x = 1
if x == 1: # we use the colon to indicate a new block of code, such as a loop, function or conditional statement
# indented four spaces, this is [tab] 
    print("x is 1.")
# Python supports two types of numbers - integers (whole numbers) (int) and floating point numbers(decimals)
myint = 90
print(myint)
# To define a floating point number, you may use one of the following notations
myfloat = 90.0
print(myfloat)
# Or this notation
myfloat = float(90)
print(myfloat)
# Strings, defined either by a single quote(') or double quote(")
mystring = 'hello'
print(mystring)
mystring = "Hello"
print(mystring)
# The difference between the two is hat using double quotes makes it easy to include apostrophes ( whereas these would terminate the string if using single quotes)
mystring = "Don't worry about apostrophes"
print(mystring)
